package com.example.kotlinnew

//this function or 'fun' is how to run the kotlin (in main)
fun main() {
    /**
     * var - mutable
     * val - immutable
     * Best practice - In essence, use val > var
     */
   //initialize variable
    val name: String
    val age: Int
    name = "James"
    age = 21

    print("Hello my name is $name I'm $age years old.")

}
